    public void loadData(ArrayList<AccountDTO> Arrays_Account) {
        DefaultTableModel tblModel = (DefaultTableModel) List_Account.getModel();
        while (tblModel.getRowCount() > 0) {
            tblModel.removeRow(0);
        }

        for (AccountDTO acc : Arrays_Account) {
            tblModel.addRow(new Object[] {
                    acc.getMaNV(), 
                    acc.getTaiKhoan(), 
                    acc.getMaNQ(),
                    acc.getTrangThai()
            });
        }
    }

